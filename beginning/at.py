x = 10  # Atribuindo o valor inicial a x
print("Valor inicial de x:", x)

x += 5  # Soma 5 ao valor de x
print("Após x += 5:", x)

x -= 3  # Subtrai 3 de x
print("Após x -= 3:", x)

x *= 2  # Multiplica x por 2
print("Após x *= 2:", x)

x /= 4  # Divide x por 4
print("Após x /= 4:", x)

x //= 2  # Realiza divisão inteira e atribui
print("Após x //= 2:", x)

x %= 3  # Calcula o módulo de x por 3
print("Após x %= 3:", x)

x **= 3  # Eleva x à potência de 3
print("Após x **= 3:", x)
